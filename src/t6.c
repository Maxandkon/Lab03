#include <stdio.h>
int main()
{
	int S, nf, ns; //Змінні
	S = 1; //Присвоюємо 1, щоб правильно знайти добуток
	nf = 50; //Найменьше число діапозона
	ns = 52; //Найбільше число діапозона
	while (nf <= ns) { //Робимо цикл
 	   S = S * nf; //Знаходимо добуток
  	  ++nf; //Додаємо 1, задля циклу While
  	  }
}
