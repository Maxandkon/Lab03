#include <stdio.h>
int main()
{
	int x, x1, x2, y1; //Змінні 
	double y; //Змінна що відображають залишок
	x = 123; //Число
	x1 = x / 100; //Ліве число
	x2 = x % 10; //Праве число
	y = (double)x1 / x2; //Знаходимо результат з безкінченим залишком
	y1 = y * 100; //Додаткова змінна щоб зробити залишок до 2
	y = (double)y1 / 100; //Результат з двома знаками після коми
}
